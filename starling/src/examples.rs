pub fn make_earth() -> Body {
    Body::with_mass(63.0, 1000.0, 15000.0)
}

pub fn make_earth_inf_soi() -> Body {
    Body::with_mass(63.0, 1000.0, 10000000.0)
}

pub fn make_luna() -> (Body, SparseOrbit) {
    (
        Body::with_mass(22.0, 10.0, 800.0),
        SparseOrbit::circular(3800.0, make_earth(), Nanotime::secs(-40), false),
    )
}

pub fn consistency_orbits(body: Body) -> Vec<SparseOrbit> {
    let mut ret = vec![];
    let r = 1000.0 * Vec2::X;
    let v0 = Vec2::new(0.0, 30.0);
    for angle in [0.6] {
        let pos = rotate(r, angle);
        for vx in (-200..=200).step_by(10) {
            for vy in (-200..=200).step_by(10) {
                let o = SparseOrbit::from_pv(
                    (pos, v0 + Vec2::new(vx as f32, vy as f32)),
                    body,
                    Nanotime::zero(),
                );
                if let Some(o) = o {
                    ret.push(o);
                }
            }
        }
    }
    ret
}

pub fn rss() -> (PlanetarySystem, ObjectIdTracker) {
    let mut id = ObjectIdTracker::new();
    let earth_body = Body::with_mu(EARTH_RADIUS, EARTH_MU, EARTH_SOI);
    let mut earth = PlanetarySystem::new(id.next_planet(), "Earth", earth_body);

    let luna_body = Body::with_mu(LUNA_RADIUS, LUNA_MU, LUNA_SOI);
    let luna = PlanetarySystem::new(id.next_planet(), "Luna", luna_body);
    let luna_orbit = SparseOrbit::circular(
        LUNA_ORBITAL_RADIUS as f64,
        earth_body,
        Nanotime::zero(),
        false,
    );

    earth.orbit(luna_orbit, luna);

    (earth, id)
}

